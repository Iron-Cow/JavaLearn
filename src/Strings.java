public class Strings {
    public static void main(String[] args) {
        int x = 5;

        String s = "Hello"; // Capital letter in String - convention to name of class (lower case for primitives)
        // s is link to string object "Hello"

        String space = " ";
        String name = "Bob";
        System.out.println(s + space + name);
        System.out.println("Hello" + " " + "John");
        System.out.println("My number is " + x);
    }
}
